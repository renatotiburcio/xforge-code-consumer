const fs = require('fs');
const path = require('path');
const fp = path.join(__dirname, '..', 'src', 'views', 'ChatViewProvider.ts');

// Ler SVGs inline
function loadSvg(name, size) {
  size = size || 14;
  try {
    const p = path.join(__dirname, '..', '..', '..', 'ref', 'icones', 'svg', name);
    let c = fs.readFileSync(p, 'utf-8').trim()
      .replace(/^<svg[^>]*>/, '')
      .replace(/<\/svg>\s*$/, '')
      .replace(/<!--.*?-->/g, '')
      .trim();
    return c;
  } catch (e) { return ''; }
}

const ICONS = {
  chevron: loadSvg('chevron-down.svg'),
  clock: loadSvg('clock.svg'),
  trash: loadSvg('trash-can.svg'),
  plus: loadSvg('square-plus.svg'),
  send: loadSvg('paper-plane-top.svg'),
  list: loadSvg('list.svg'),
  user: loadSvg('user.svg'),
  code: loadSvg('code.svg'),
  folder: loadSvg('folder.svg'),
  branch: loadSvg('code-branch.svg'),
  file: loadSvg('file.svg'),
};

let out = '';
out += '// AUTO-GENERATED by scripts/build-icons-data.ts\n';
out += 'export const ICON_DATA = ' + JSON.stringify(ICONS, null, 2) + ';\n';
out += 'export const ICON_SIZE = 16;\n';

fs.writeFileSync(path.join(__dirname, '..', 'src', 'webview', 'icon-data.ts'), out, 'utf-8');
console.log('OK');
